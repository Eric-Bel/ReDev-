// чтобы вернуть обратную последовательность от n до 1

// Например n = 5 на выходе у тебя должно быть 5,4,3,2,1

let numFor = 5;

function lilFn(num) {
    let arr = [];
    for (; num > 0; --num) {
        arr.push(num)
    }
    return arr
};
